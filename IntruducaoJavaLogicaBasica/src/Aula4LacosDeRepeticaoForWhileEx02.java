public class Aula4LacosDeRepeticaoForWhileEx02 {
    public static void main(String[] args) {
        double chakra = 100.0;
        while (chakra > 0){
            System.out.println("Lançando Jutsu!!");
            chakra -= 25;
            System.out.println("Jutsu lançado !! Chakra Restante: " + chakra);

        }
    }
}
